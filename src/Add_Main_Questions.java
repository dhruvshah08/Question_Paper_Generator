    private boolean checkDiscrepancy(){
        int numberOfMarks=Paper.getTotalMarks();//got max marks of paper as allocated
        int totalMarks=0;
        for(Main_Question main_Question:Paper.getListOfMainQuestions()){
            totalMarks+=main_Question.getWeightage();
        }
        if(numberOfMarks!=totalMarks){
            return false;
        }
        return true;
    } 
    private void btnGeneratePaperActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGeneratePaperActionPerformed
        // TODO add your handling code here:
        //code to generate a paper would begin from here!
        //make the checks here of marks,noOfQuestions!
        //a thread would begin from here!
        for(Main_Question main_Question:Paper.getListOfMainQuestions()){
            main_Question.clearList();
        }
        try{
            executorService=Executors.newSingleThreadExecutor();
            if(checkDiscrepancy()){
                Future<Boolean> future=executorService.submit(new PaperGeneratingThread());
                btnGeneratePaper.setEnabled(false);
                if(!future.get()){
                    JOptionPane.showMessageDialog(null, "Questions in the bank not enough!");
                }else{
                    JOptionPane.showMessageDialog(null, "Paper Generated Successfully!");
                }
            }else{
                JOptionPane.showMessageDialog(null, "There is a discrepancy in marks!");
            }
            btnGeneratePaper.setEnabled(true);
        }catch(InterruptedException|ExecutionException e){
            
        }
    }//GEN-LAST:event_btnGeneratePaperActionPerformed
    
