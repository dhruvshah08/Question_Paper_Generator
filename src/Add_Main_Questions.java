    public void refreshMainQuestions(){
        String[] colHeads ={"Question Number","Statement", "Total Weightage", "Number of Questions"};
        String contentHolder[][]=new String[Paper.getListOfMainQuestions().size()][4];
        int counter=0;
        for(Main_Question main_Question:Paper.getListOfMainQuestions()){
           String arr[]={main_Question.getAllotedQuestionNumber()+"",main_Question.getStatement(),main_Question.getWeightage()+"",main_Question.getNoOfQuestions()+""};
           contentHolder[counter]=arr;
           counter++;
        }
        jTableData.setModel(new DefaultTableModel(contentHolder, colHeads));
        if(counter==0){
            btnGeneratePaper.setEnabled(false);
             btnDelete.setEnabled(false);
             btnUpdate.setEnabled(false);
        }else{
             btnGeneratePaper.setEnabled(true);
        }
    }
